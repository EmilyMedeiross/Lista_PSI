# Função para somar valores 
def somar(x, y): 
   resultado = int(x) + int(y)
   return resultado 

# Função para subtrair valores 

def subtracao(x, y): 
   resultado = int(x)- int(y)
   return resultado 

# Função para multiplicar valores 

def multiplicar(x, y):
   resultado = int(x) * int(y)
   return resultado  

#Função para dividir valores

def dividir(x, y):
   resultado = int(x) % int(y)
   return resultado 


